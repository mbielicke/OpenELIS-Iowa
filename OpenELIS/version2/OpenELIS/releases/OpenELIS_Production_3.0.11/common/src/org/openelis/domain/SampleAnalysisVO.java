package org.openelis.domain;

import java.io.Serializable;
import java.util.Date;

import org.openelis.ui.common.DataBaseUtil;
import org.openelis.ui.common.Datetime;

/**
 * This class is used for holding minimal sample and analysis data for display
 * in lookup tables.
 */
public class SampleAnalysisVO implements Serializable {

    private static final long serialVersionUID = 1L;

    protected Integer         accessionNumber, analysisId, sampleId, testId;
    protected String          methodName, testName;
    protected Datetime        receivedDate;

    public SampleAnalysisVO() {
    }

    public SampleAnalysisVO(Integer sampleId, Integer accessionNumber, Date receivedDate,
                            Integer analysisId, Integer testId, String testName, String methodName) {
        setSampleId(sampleId);
        setAccessionNumber(accessionNumber);
        setReceivedDate(DataBaseUtil.toYM(receivedDate));
        setAnalysisId(analysisId);
        setTestId(testId);
        setTestName(testName);
        setMethodName(methodName);
    }

    public Integer getSampleId() {
        return sampleId;
    }

    public void setSampleId(Integer sampleId) {
        this.sampleId = sampleId;
    }

    public Integer getAccessionNumber() {
        return accessionNumber;
    }

    public void setAccessionNumber(Integer accessionNumber) {
        this.accessionNumber = accessionNumber;
    }

    public Datetime getReceivedDate() {
        return receivedDate;
    }

    public void setReceivedDate(Datetime receivedDate) {
        this.receivedDate = DataBaseUtil.toYM(receivedDate);
    }

    public Integer getAnalysisId() {
        return analysisId;
    }

    public void setAnalysisId(Integer analysisId) {
        this.analysisId = analysisId;
    }

    public Integer getTestId() {
        return testId;
    }

    public void setTestId(Integer testId) {
        this.testId = testId;
    }

    public String getTestName() {
        return testName;
    }

    public void setTestName(String testName) {
        this.testName = DataBaseUtil.trim(testName);
    }

    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = DataBaseUtil.trim(methodName);
    }
}